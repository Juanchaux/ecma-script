let hello = 'Hello';
let world = 'World';
let epicPhrase = hello + ' ' + world;
console.log(epicPhrase);

// Template literals

let epicPhrase2 = `${hello} ${world}!`;
console.log(epicPhrase2);

// Multiline strings

let lorem = 'esto es un string \n' + 'con saltos de linea';
console.log(lorem);

let lorem2 = `esta es una frase epica
con saltos de linea`;
console.log(lorem2);